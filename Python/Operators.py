#ArthimeticOperator
#(+,-,*,%,/,//)

a = 10

b = 20

print(a + b)

print(a - b)

print(a * b)

print(a % b) # % -> this symbol gives remainder.

print(a / b) # / -> this symbol gives quetioent and also will get values in points.

print(a // b) # // -> this symbol gives quetioent and we get the int value not point values.

print(a ** b) # ** -> this symbol gives a power of b of the value.


